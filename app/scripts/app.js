'use strict';

/**
 * @ngdoc overview
 * @name demoApp
 * @description
 * # demoApp
 *
 * Main module of the application.
 */
angular
  .module('demoApp', [
    'ngRoute'
  ])
  .config(function ($routeProvider) {
    $routeProvider
      .when('/', {
        templateUrl: 'views/main.html',
        controller: 'MainCtrl',
        dependencies: ["scripts/constants/main-constant.js",
          "scripts/service/main-service.js"]
      })
      .otherwise({
        redirectTo: '/'
      });
  });
